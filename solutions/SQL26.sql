-- 26. 입양 시각 구하기(1)

SELECT HOUR(DATETIME) HOUR,
        COUNT(DATETIME)
FROM ANIMAL_OUTS 
WHERE HOUR(DATETIME) BETWEEN 9 AND 19
GROUP BY 1
ORDER BY 1



SELECT DATE_FORMAT(DATETIME, '%H') HOUR,
        COUNT(DATETIME)
FROM ANIMAL_OUTS 
WHERE DATE_FORMAT(DATETIME, '%H') BETWEEN 9 AND 19
GROUP BY 1
ORDER BY 1
