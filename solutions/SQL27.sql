-- 27. 진료과별 총 예약 횟수 출력하기

SELECT MCDP_CD '진료과코드',
        COUNT(APNT_YMD) '5월예약건수'
FROM APPOINTMENT
WHERE APNT_YMD LIKE '2022-05-%'
GROUP BY 1
ORDER BY 2 ASC, 1



SELECT MCDP_CD '진료과코드',
        COUNT(APNT_YMD) '5월예약건수'
FROM APPOINTMENT
WHERE DATE_FORMAT(APNT_YMD, '%Y-%m-%') = '2022-05-%'
GROUP BY 1
ORDER BY 2, 1
